# Napišite program koji učitava stranicu kvadrata, zatim ispisuje opseg i površinu kvadrata

stranicaKvadrata = int(input("Molim vas da unesete stranicu kvadrata: "))

opsegKvadrata = 4 * stranicaKvadrata
povrsinaKvadrata = stranicaKvadrata**2

print(f"Stranica kvadrata je: {stranicaKvadrata}. Opseg kvadrata je {opsegKvadrata}. Površina kvadrata je {povrsinaKvadrata}")